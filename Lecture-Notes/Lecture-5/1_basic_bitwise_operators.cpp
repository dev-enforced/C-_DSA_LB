/*The following bitwise operators are used here

Bitwise AND : & ,
Bitwise OR: | ,
Bitwise NOT: ~,
Bitwise XOR: ^

*/

#include<iostream>
using namespace std;
int main(){
    int a=4,b=6;
    cout<<"a&b: "<<(a&b) << endl;
    cout<<"a|b: "<<(a|b) <<endl;
    cout<<"~a: "<<(~a)<<endl;
    cout<<"a^b: "<<(a^b) << endl;
}